let resultado = "Aprovada";

switch (resultado) {
  case "Aprovada":
    console.log("Parabéns, você foi aprovada no processo seletivo!");
    break;

  case "Lista":
    console.log("Você está na lista!");
    break;

  case "Reprovada":
    console.log("Infelizmente você foi reprovada!");
    break;

  default:
    console.log("Não se aplica!");
};